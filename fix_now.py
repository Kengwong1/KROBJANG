def inject_search(entries):
    print("\n[3/3] 🔎 ฝัง search ใน index.html ...")

    json_data = json.dumps(entries, ensure_ascii=False, separators=(',',':'))

    # search script — เขียนเป็น string ธรรมดา ไม่ใช้ f-string เพื่อหลีกเลี่ยง brace ขัดแย้ง
    search_script = (
        "<script id=\"searchScript\">\n"
        "(function(){\n"
        "  var DATA=" + json_data + ";\n"
        "  var inp=document.getElementById('search-input');\n"
        "  var box=document.getElementById('search-results');\n"
        "  if(!inp||!box){console.warn('search: ไม่พบ #search-input หรือ #search-results');return;}\n"
        "  box.style.cssText='position:absolute;top:115%;left:0;right:0;background:#fff;'\n"
        "    +'border-radius:14px;box-shadow:0 12px 32px rgba(0,0,0,.28);'\n"
        "    +'z-index:9999;overflow:hidden;max-height:380px;overflow-y:auto;display:none;';\n"
        "  var timer;\n"
        "  inp.addEventListener('input',function(){\n"
        "    clearTimeout(timer);\n"
        "    timer=setTimeout(function(){\n"
        "      var q=inp.value.trim().toLowerCase();\n"
        "      box.innerHTML='';\n"
        "      if(!q){box.style.display='none';return;}\n"
        "      var hits=DATA.filter(function(a){\n"
        "        return (a.t||'').toLowerCase().indexOf(q)>=0||(a.cat||'').toLowerCase().indexOf(q)>=0;\n"
        "      }).slice(0,10);\n"
        "      if(!hits.length){\n"
        "        box.innerHTML='<div style=\"padding:.75rem 1rem;color:#94a3b8;font-size:.85rem;\">ไม่พบบทความ</div>';\n"
        "        box.style.display='block';return;\n"
        "      }\n"
        "      hits.forEach(function(a){\n"
        "        var el=document.createElement('a');\n"
        "        el.href=a.u;\n"
        "        el.style.cssText='display:flex;gap:.6rem;align-items:center;padding:.6rem .9rem;'\n"
        "          +'border-bottom:1px solid #f1f5f9;text-decoration:none;color:#1e293b;';\n"
        "        el.onmouseover=function(){this.style.background='#f8fafc';};\n"
        "        el.onmouseout=function(){this.style.background='';};\n"
        "        var img=document.createElement('img');\n"
        "        img.src=a.img||'';\n"
        "        img.loading='lazy';\n"
        "        img.style.cssText='width:52px;height:38px;object-fit:cover;border-radius:5px;flex-shrink:0;';\n"
        "        img.onerror=function(){this.style.display='none';};\n"
        "        var info=document.createElement('div');\n"
        "        var title=document.createElement('div');\n"
        "        title.style.cssText='font-weight:600;font-size:.88rem;line-height:1.3;';\n"
        "        title.textContent=a.t||'';\n"
        "        var cat=document.createElement('div');\n"
        "        cat.style.cssText='font-size:.72rem;color:#94a3b8;margin-top:.1rem;';\n"
        "        cat.textContent=a.cat||'';\n"
        "        info.appendChild(title);\n"
        "        info.appendChild(cat);\n"
        "        el.appendChild(img);\n"
        "        el.appendChild(info);\n"
        "        box.appendChild(el);\n"
        "      });\n"
        "      box.style.display='block';\n"
        "    },180);\n"
        "  });\n"
        "  document.addEventListener('click',function(e){\n"
        "    if(!box.contains(e.target)&&e.target!==inp)box.style.display='none';\n"
        "  });\n"
        "  inp.addEventListener('keydown',function(e){\n"
        "    if(e.key==='Escape'){box.style.display='none';inp.value='';}\n"
        "  });\n"
        "})();\n"
        "</script>"
    )

    idx = BASE_PATH / "index.html"
    if not idx.exists():
        print("   ❌ ไม่พบ index.html")
        return

    html = idx.read_text(encoding="utf-8", errors="ignore")
    orig = html

    # ลบ searchScript เก่า (ถ้ามี)
    html = re.sub(r'<script id="searchScript">.*?</script>', '', html, flags=re.DOTALL)

    # ลบ search script แบบเก่าที่ใช้ fetch หรือ inline DATA เก่า
    html = re.sub(
        r'<script>\s*\(function\(\)\s*\{[\s\S]{0,200}?(?:search-index\.json|var DATA=\[)[\s\S]*?\}\)\(\);\s*</script>',
        '', html, flags=re.DOTALL
    )
    # ลบ try { fetch pattern
    html = re.sub(
        r'<script>\s*(?://[^\n]*)?\s*try\s*\{[\s\S]{0,100}?fetch\([\'"]search-index[\s\S]*?</script>',
        '', html, flags=re.DOTALL
    )

    # ฝัง script ใหม่ก่อน </body>
    if "</body>" in html:
        html = html.replace("</body>", search_script + "\n</body>", 1)
    else:
        html += "\n" + search_script

    if html != orig:
        idx.write_text(html, encoding="utf-8")
        print("   ✅ ฝัง search script ใน index.html สำเร็จ")
    else:
        print("   ℹ️  ไม่มีการเปลี่ยนแปลง (อาจมีอยู่แล้ว)")

    # ตรวจสอบว่า search-input และ search-results มีอยู่ใน HTML
    if 'id="search-input"' not in html and "id='search-input'" not in html:
        print("   ⚠️  ไม่พบ id=\"search-input\" ใน index.html!")
        print("      → รัน: python agent_fix_v5.py  เพื่อ rebuild index.html ก่อน")
    else:
        print("   ✅ พบ #search-input ใน index.html")

    if 'id="search-results"' not in html and "id='search-results'" not in html:
        print("   ⚠️  ไม่พบ id=\"search-results\" ใน index.html!")
    else:
        print("   ✅ พบ #search-results ใน index.html")


