_NON_ARTICLE = {
    "index.html","404.html","contact.html","sitemap.html","privacy.html",
    "style.css","nav.js","robots.txt","sitemap.xml","search-index.json",
    "news.html","lifestyle.html","health.html","food.html","finance.html",
    "technology.html","entertainment.html","ai.html","sport.html",
    "horoscope.html","entertainment.html","booking.html","gaming.html",
}

def _picsum(stem):
    seed = hashlib.md5(stem.encode()).hexdigest()[:8]
    return "https://picsum.photos/seed/" + seed + "/400/225"

def scan_articles():
    result = []
    for fp in sorted(BASE_PATH.glob("*.html")):
        if fp.name in _NON_ARTICLE:
            continue
        try:
            txt = fp.read_text(encoding="utf-8", errors="ignore")
            if "<html" in txt.lower() and len(txt) > 100:
                result.append(fp)
        except Exception:
            pass
    return result

def get_title(txt, fp):
    m = re.search(r'<h1[^>]*>(.*?)</h1>', txt, re.DOTALL | re.IGNORECASE)
    if m:
        return re.sub(r'<[^>]+>', '', m.group(1)).strip()
    m = re.search(r'<title[^>]*>(.*?)</title>', txt, re.DOTALL | re.IGNORECASE)
    if m:
        t = re.sub(r'<[^>]+>', '', m.group(1)).strip()
        return re.split(r'\s*[—|–|-]\s*', t)[0].strip()
    return fp.stem.replace("_", " ").replace("-", " ").title()

def get_img(txt, fp):
    m = re.search(r'<meta[^>]+property=["\']og:image["\'][^>]+content=["\']([^"\']+)["\']', txt, re.IGNORECASE)
    if m: return m.group(1).strip()
    m = re.search(r'<img[^>]+src=["\']([^"\']+)["\']', txt, re.IGNORECASE)
    if m:
        s = m.group(1).strip()
        if "icon" not in s.lower() and "logo" not in s.lower():
            return s
    return _picsum(fp.stem)

def get_cat(fp):
    stem = fp.stem
    cat  = stem.split("_")[0] if "_" in stem else stem
    return หมวด_ไทย.get(cat, cat)

def build_search_index():
    print("\n[2/3] 🔍 สร้าง search index ...")
    articles = scan_articles()
    entries  = []
    for fp in articles:
        try:
            txt = fp.read_text(encoding="utf-8", errors="ignore")
            entries.append({
                "t":   get_title(txt, fp),
                "u":   fp.name,
                "img": get_img(txt, fp),
                "cat": get_cat(fp),
            })
        except Exception as e:
            print(f"   ⚠️  skip {fp.name}: {e}")

    out = BASE_PATH / "search-index.json"
    out.write_text(json.dumps(entries, ensure_ascii=False, separators=(',',':')), encoding="utf-8")
    print(f"   ✅ search-index.json: {len(entries)} รายการ")
    return entries


